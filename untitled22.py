class hashtable:
    def __init__(self,size):
      self.size=size
      self.table=[]
      for i in range(size):
        self.table.append([])

    def hash_function(self,key):
      return key % self.size

    def insert(self,key=35):
      index=self.hash_function(key)
      self.table[index].append(key)

